package yenipabka;

public class WhileLoopExample2 {

    public static void main(String[] args) {


        int geriSayim = 10;
        System.out.println("Geri sayım başlayır:");
        while (geriSayim >= 0) {
            System.out.println(geriSayim);
            geriSayim--;


        }
        System.out.println("Partladı!");

    }


}
